@click.command('seed-courses')
@with_appcontext
def seed_courses():
    """Seed database with course data from hardcoded templates."""
    trainers_map = {}
    for data in SEED_TRAINERS:
        existing = Trainer.query.filter_by(slug=data['slug']).first()
        if existing:
            click.echo(f'  Тренер вже існує: {existing.full_name}')
            trainers_map[data['slug']] = existing
            continue

        trainer = Trainer(**data)
        db.session.add(trainer)
        db.session.flush()
        trainers_map[data['slug']] = trainer
        click.echo(f'  + Тренер: {trainer.full_name}')

    for data in SEED_EVENTS:
        existing = Event.query.filter_by(slug=data['slug']).first()
        if existing:
            click.echo(f'  Захід вже існує: {existing.title}')
            continue

        trainer_slug = data.pop('trainer_slug')
        program_data = data.pop('program')

        event = Event(
            trainer=trainers_map.get(trainer_slug),
            **data,
        )
        db.session.add(event)
        db.session.flush()

        for idx, block_data in enumerate(program_data):
            block = ProgramBlock(
                event=event,
                heading=block_data['heading'],
                items=block_data['items'],
                sort_order=idx,
            )
            db.session.add(block)

        click.echo(f'  + Захід: {event.title} ({len(program_data)} блоків програми)')

    db.session.commit()
    click.echo('Seed завершено.')
