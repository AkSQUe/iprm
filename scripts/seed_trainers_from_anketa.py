def upsert(data):
    slug = data['slug']
    trainer = Trainer.query.filter_by(slug=slug).first()
    action = 'updated' if trainer else 'created'

    if not trainer:
        trainer = Trainer(slug=slug)
        db.session.add(trainer)

    trainer.full_name = data['full_name']
    trainer.role = data['role']
    trainer.experience_years = data['experience_years']
    trainer.photo = data['photo']
    trainer.is_active = True

    if not data.get('preserve_bio'):
        trainer.bio = data['bio']

    return action, trainer


def main():
    app = create_app()
    with app.app_context():
        results = []
        for entry in TRAINERS:
            action, trainer = upsert(entry)
            results.append((action, trainer))

        db.session.commit()

        for action, trainer in results:
            print(f'{action:<8} {trainer.id:>3}  {trainer.slug:<35}  {trainer.full_name}')


if __name__ == '__main__':
    main()
