def upsert_course(data, trainer_id_by_slug):
    course = Course.query.filter_by(slug=data['slug']).first()
    action = 'updated' if course else 'created'

    if not course:
        course = Course(slug=data['slug'])
        db.session.add(course)

    course.title = data['title']
    course.subtitle = data['subtitle']
    course.short_description = data['short_description']
    course.event_type = data['event_type']
    course.base_price = data['base_price']
    course.max_participants = data['max_participants']
    course.target_audience = data['target_audience']
    course.tags = data['tags']
    course.is_active = True

    trainer_slug = data.get('trainer_slug')
    if trainer_slug:
        course.trainer_id = trainer_id_by_slug.get(trainer_slug)

    # ProgramBlock: повне переписування -- видаляємо старі, створюємо нові.
    db.session.flush()  # потрібно щоб course.id був присвоєний для нових
    ProgramBlock.query.filter_by(course_id=course.id).delete()
    for sort_idx, block in enumerate(data['program_blocks']):
        db.session.add(ProgramBlock(
            course_id=course.id,
            heading=block['heading'],
            items=block['items'],
            sort_order=sort_idx,
        ))

    return action, course


def upsert_instance(course, year, month, day, fmt, price, location=''):
    start = kyiv_dt(year, month, day)
    # Idempotent ключ: (course_id, start_date, location). Дозволяє два проведення
    # одного курсу на ту саму дату в різних локаціях (напр. 15.04 Київ + Варшава).
    existing = CourseInstance.query.filter_by(
        course_id=course.id, start_date=start, location=location,
    ).first()
    if existing:
        existing.event_format = fmt
        if price is not None:
            existing.price = price
        if existing.status == 'draft':
            existing.status = 'published'
        return 'kept', existing

    inst = CourseInstance(
        course_id=course.id,
        start_date=start,
        event_format=fmt,
        price=price,
        max_participants=course.max_participants,
        trainer_id=course.trainer_id,
        status='published',
        location=location,
    )
    db.session.add(inst)
    return 'created', inst


