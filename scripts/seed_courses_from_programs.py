        instance_results = []
        for entry in SCHEDULE:
            # 6-tuple (Київ, location='') або 7-tuple з location
            if len(entry) == 6:
                slug, year, month, day, fmt, price = entry
                location = ''
            else:
                slug, year, month, day, fmt, price, location = entry
            course = course_by_slug.get(slug)
            if not course:
                print(f'  ! WARN: розклад посилається на slug={slug!r}, такого '
                      f'курсу немає -- пропускаю')
                continue
            action, inst = upsert_instance(
                course, year, month, day, fmt, price, location,
            )
            instance_results.append((action, slug, inst))

        db.session.commit()

        print()
        print('=== Courses ===')
        for action, c in course_results:
            print(f'  {action:<8} id={c.id:<3} {c.slug}')

        print()
        print('=== Instances ===')
        for action, slug, inst in instance_results:
            d = inst.start_date.strftime('%Y-%m-%d')
            loc = inst.location or '(Київ)'
            print(f'  {action:<8} {d}  {inst.event_format:<7} {loc:<10} {slug}')

        print()
        print(f'Total courses: {len(course_results)}, '
              f'instances: {len(instance_results)}')


